$(function() {
  var A = parseInt($('#leftside').width(), 10),
    B = parseInt($('#rightside').width(), 10),
    Z = parseInt($('#draggablediv').width(), 10),
    minw = parseInt((A + B + Z) * 10 / 100, 10),
    offset = $('#container').offset(),
    splitter = function(event, ui) {
      var aw = parseInt(ui.position.left),
          bw = A + B - aw;
  
      var leftSideCSS = {width : aw},
        rightSideCSS = {width : bw},
        $leftSide = $('#leftside'),
        $rightSide = $('#rightside');
  
  
      if (1329 > $leftSide.width()) {
        leftSideCSS['z-index'] = 0;
        rightSideCSS['z-index'] = 1;
      }
      else {
        leftSideCSS['z-index'] = 1;
        rightSideCSS['z-index'] = 0;
      }
  
      $leftSide.css(leftSideCSS);
      $rightSide.css(rightSideCSS);
  
  
    };
  $('#draggablediv').draggable({
    iframeFix: true,
    axis : 'x',
    containment : [
      offset.left + minw,
      offset.top,
      offset.left + A + B - minw,
      offset.top + $('#container').height()
    ],
    drag : splitter
  });
});

