function openFrame(evt, cityName) {
  var i, iframecontent, iframebutton;
  iframecontent = document.getElementsByClassName("iframecontent");
  for (i = 0; i < iframecontent.length; i++) {
    iframecontent[i].style.display = "none";
  }
  iframebutton = document.getElementsByClassName("iframebutton");
  for (i = 0; i < iframebutton.length; i++) {
    iframebutton[i].className = iframebutton[i].className.replace(" active", "");
  }
  document.getElementById(cityName).style.display = "block";
  evt.currentTarget.className += " active";
}
